#include<iostream>
#include<string>
using namespace std;
int main(){
    // 作用 : 用于从键盘获取数据
    // 关键字 ：cin
    // 语法 : cin >> 变量

    // 整形输入
    int a = 0;
    cout << "请输入整型变量: ";
    cin >> a;
    cout << a << endl;

    // 浮点型输入
    double b = 0.0f;
    cout << "请输入浮点型变量: ";
    cin >> b;
    cout << b << endl;

    // 字符型输入
    char ch = 0;
    cout << "请输入字符型变量: ";
    cin >> ch;
    cout <<ch << endl;

    // 字符串型输入
    string str;
    cout << "请输入字符串型变量: ";
    cin >> str;
    cout << str << endl;

    // 布尔类型输入
    bool  flag = true;
    cout << "请输入布尔型变量: ";
    cin >> flag;
    cout << flag << endl;
    return 0;
}
